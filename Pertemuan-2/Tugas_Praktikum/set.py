fruits = {"apple", "banana", "cherry"}
print(fruits)

fruits = {"apple", "banana", "cherry"}
fruits.add("star fruit")
print(fruits)

fruits = {"apple", "banana", "cherry", "star fruit"}
fruits.remove("star fruit")
print(fruits)

fruits = {"apple", "banana", "cherry", "star fruit"}
fruits.discard("star fruit")
print(fruits)